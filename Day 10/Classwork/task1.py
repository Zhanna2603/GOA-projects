# , + , - , / , // , %


# /
print(10/2)#5.0
print(30/7)#4.285714285714286

# //
# We can write // in order not to specify an integer value separatly.
# print(10//2) == print(int(10//2))
# outputs only the whole part
 
print(10//2)#5
print(30//7)#4

# % outputs the remainder
print(10%2)#0
print(30%7)#2

# == compares two numbers (True/False)
print(10==10)#True
print(30==7)#False

# != compares two numbers (True/False)
print(5!=5)#False